#include <assert.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <time.h>

int main(int argc, char *argv[])
{
	char str[55];

	time_t now;

	time ( &now );

	strcpy ( str, ctime ( &now ) );

	printf ( "%s", str );

	return EXIT_SUCCESS;
}

